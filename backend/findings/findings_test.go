// An over-long note is refused, not truncated: silently dropping the tail of an analyst's
// reasoning is worse than saying it did not fit.
func TestOverlongNoteIsRefusedNotTruncated(t *testing.T) {
	s := newStore(t)
	long := strings.Repeat("x", consts.MaxFindingNoteLen+1)
	if _, err := s.Set(Finding{Key: "hash-a", Note: long}, at(0)); err != ErrTooLong {
		t.Fatalf("err = %v, want ErrTooLong", err)
	}
	if s.Get("hash-a") != nil {
		t.Error("refused write still stored a finding")
	}
}

func TestTagsAreNormalizedAndDeduplicated(t *testing.T) {
	s := newStore(t)
	got := mustSet(t, s, Finding{
		Key:  "hash-a",
		Tags: []string{"Lateral Movement", "  lateral movement ", "", "Persistence"},
	}, at(0))

	want := []string{"lateral movement", "persistence"}
	if len(got.Tags) != len(want) {
		t.Fatalf("tags = %v, want %v", got.Tags, want)
	}
	for i, w := range want {
		if got.Tags[i] != w {
			t.Fatalf("tags = %v, want %v", got.Tags, want)
		}
	}
}

func TestTagCountIsCapped(t *testing.T) {
	s := newStore(t)
	many := make([]string, consts.MaxFindingTags+10)
	for i := range many {
		many[i] = string(rune('a'+i%26)) + strings.Repeat("z", i)
	}
	got := mustSet(t, s, Finding{Key: "hash-a", Tags: many}, at(0))
	if len(got.Tags) > consts.MaxFindingTags {
		t.Fatalf("stored %d tags, cap is %d", len(got.Tags), consts.MaxFindingTags)
	}
}


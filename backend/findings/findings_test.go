// The sidecar records which hash_normalized recipe its keys were written against, so a
// future change to that recipe is detectable rather than silently orphaning every finding.
func TestSidecarRecordsTheHashVersion(t *testing.T) {
	dir := t.TempDir()
	s, err := Open(dir)
	if err != nil {
		t.Fatalf("Open: %v", err)
	}
	mustSet(t, s, Finding{Key: "hash-a", Flagged: true}, at(0))

	if s.HashVersion() != consts.FindingsHashVersion {
		t.Errorf("HashVersion = %d, want %d", s.HashVersion(), consts.FindingsHashVersion)
	}
	if s.Stale() {
		t.Error("a sidecar written by this build reports itself stale")
	}
	data, err := os.ReadFile(filepath.Join(dir, findingsFile))
	if err != nil {
		t.Fatal(err)
	}
	if !strings.Contains(string(data), "hash_version") {
		t.Error("sidecar does not record the hash version")
	}
}

// A sidecar from a different recipe still LOADS — the notes are irreplaceable — but reports
// itself stale, because none of its keys can match a live event.
func TestForeignHashVersionLoadsButReportsStale(t *testing.T) {
	dir := t.TempDir()
	doc := `{"hash_version": 99, "findings": {"hash-a": {"key":"hash-a","flagged":true,"note":"keep me"}}}`
	if err := os.WriteFile(filepath.Join(dir, findingsFile), []byte(doc), 0o644); err != nil {
		t.Fatal(err)
	}

	s, err := Open(dir)
	if err != nil {
		t.Fatalf("Open: %v", err)
	}
	if !s.Stale() {
		t.Error("a sidecar from another recipe does not report itself stale")
	}
	if s.HashVersion() != 99 {
		t.Errorf("HashVersion = %d, want the stored 99", s.HashVersion())
	}
	if got := s.Get("hash-a"); got == nil || got.Note != "keep me" {
		t.Fatalf("stale sidecar dropped its findings: %+v", got)
	}
}

// A sidecar predating versioning is adopted at the current version rather than treated as

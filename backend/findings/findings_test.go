func TestConcurrentUseIsSafe(t *testing.T) {
	s := newStore(t)
	done := make(chan struct{})
	for i := 0; i < 8; i++ {
		go func(i int) {
			defer func() { done <- struct{}{} }()
			key := string(rune('a' + i))
			_, _ = s.Set(Finding{Key: key, Flagged: true, Tags: []string{key}}, at(i))
			_ = s.Get(key)
			_ = s.List()
			_ = s.Tags()
			_ = s.Stats()
			_, _ = s.Keys(consts.FindingFilterFlagged)
		}(i)
	}
	for i := 0; i < 8; i++ {
		<-done
	}
	if s.Stats().Total != 8 {
		t.Fatalf("total = %d, want 8", s.Stats().Total)
	}
}

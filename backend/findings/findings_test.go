func TestKeysByFilter(t *testing.T) {
	s := newStore(t)
	mustSet(t, s, Finding{Key: "flagged-only", Flagged: true}, at(0))
	mustSet(t, s, Finding{Key: "noted-only", Note: "why"}, at(1))
	mustSet(t, s, Finding{Key: "tagged-only", Tags: []string{"t"}}, at(2))

	cases := []struct {
		filter string
		want   []string
	}{
		{consts.FindingFilterFlagged, []string{"flagged-only"}},
		{consts.FindingFilterNoted, []string{"noted-only"}},
		{consts.FindingFilterAnnotated, []string{"flagged-only", "noted-only", "tagged-only"}},
	}
	for _, c := range cases {
		got, ok := s.Keys(c.filter)
		if !ok {
			t.Fatalf("%s: ok = false", c.filter)
		}
		if strings.Join(got, ",") != strings.Join(c.want, ",") {
			t.Errorf("%s: keys = %v, want %v", c.filter, got, c.want)
		}
	}
}


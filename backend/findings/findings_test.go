// Clearing the last piece of content deletes the finding, so the event does not keep
// counting as annotated in filters and stats.
func TestClearingAllContentRemovesTheFinding(t *testing.T) {
	s := newStore(t)
	mustSet(t, s, Finding{Key: "hash-a", Flagged: true, Tags: []string{"t"}, Note: "n"}, at(0))

	got := mustSet(t, s, Finding{Key: "hash-a"}, at(5))
	if got != nil {
		t.Fatalf("emptied finding returned %+v, want nil", got)
	}
	if s.Get("hash-a") != nil {
		t.Error("emptied finding is still stored")
	}
	if s.Stats().Total != 0 {
		t.Error("emptied finding still counts in stats")
	}
}

